    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Long id = Long.parseLong(txtClave.getText());

    DAOEmpleados dao = new DAOEmpleados();
    Empleados eliminado = dao.eliminar(id);

    if (eliminado != null) {
        JOptionPane.showMessageDialog(this, "Empleado eliminado con clave: " + eliminado.getClave());
        // limpiar campos
        txtClave.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "No se encontr√≥ un empleado con esa clave.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

